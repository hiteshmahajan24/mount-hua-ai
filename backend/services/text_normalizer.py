import re

# ============================================
# NORMALIZE TEXT
# ============================================

def normalize_text(text):

    # ========================================
    # REMOVE EXTRA SPACES
    # ========================================

    text = re.sub(
        r"\s+",
        " ",
        text
    )

    # ========================================
    # NORMALIZE LONG SCREAMS
    # ========================================

    text = re.sub(

        r'A{4,}',

        'Aahhh',

        text

    )

    text = re.sub(

        r'AH{3,}',

        'Aahh',

        text

    )

    text = re.sub(

        r'H{5,}',

        'hh',

        text

    )

    # ========================================
    # NORMALIZE LAUGHTER
    # ========================================

    text = re.sub(

        r'(ha){3,}',

        'haha',

        text,

        flags=re.IGNORECASE

    )

    text = re.sub(

        r'(he){3,}',

        'hehe',

        text,

        flags=re.IGNORECASE

    )

    # ========================================
    # UGH / GROANS
    # ========================================

    text = re.sub(

        r'Ug+h+',

        'Ugh',

        text,

        flags=re.IGNORECASE

    )

    # ========================================
    # DOTS / PAUSES
    # ========================================

    text = re.sub(

        r'\.{3,}',

        '...',

        text

    )

    # ========================================
    # EXCESSIVE EXCLAMATION
    # ========================================

    text = re.sub(

        r'!{2,}',

        '!',

        text

    )

    # ========================================
    # EXCESSIVE QUESTION MARKS
    # ========================================

    text = re.sub(

        r'\?{2,}',

        '?',

        text

    )

    # ========================================
    # STRANGE SYMBOL CHAINS
    # ========================================

    text = re.sub(

        r'[-=_]{3,}',

        '',

        text

    )

    # ========================================
    # REMOVE WEIRD ASCII NOISE
    # ========================================

    text = re.sub(

        r'[\*#@]{2,}',

        '',

        text

    )

    # ========================================
    # CLEAN SPACING
    # ========================================

    text = re.sub(

        r'\s+([.,!?])',

        r'\1',

        text

    )

    return text.strip()